package model;

public class Theater {
	
	private Long theaterNum;
	private Long movieNum;
	private String theaterName;
	private String theaterArea;
	private String theaterAdd1;
	private String theaterAdd2;
	
	
	public String getTheaterAdd1() {
		return theaterAdd1;
	}
	public void setTheaterAdd1(String theaterAdd1) {
		this.theaterAdd1 = theaterAdd1;
	}
	public String getTheaterAdd2() {
		return theaterAdd2;
	}
	public void setTheaterAdd2(String theaterAdd2) {
		this.theaterAdd2 = theaterAdd2;
	}
	public Long getTheaterNum() {
		return theaterNum;
	}
	public void setTheaterNum(Long theaterNum) {
		this.theaterNum = theaterNum;
	}
	public Long getMovieNum() {
		return movieNum;
	}
	public void setMovieNum(Long movieNum) {
		this.movieNum = movieNum;
	}
	public String getTheaterName() {
		return theaterName;
	}
	public void setTheaterName(String theaterName) {
		this.theaterName = theaterName;
	}
	public String getTheaterArea() {
		return theaterArea;
	}
	public void setTheaterArea(String theaterArea) {
		this.theaterArea = theaterArea;
	}
	
	
	
}
